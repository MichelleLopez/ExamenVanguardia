
public class MovieDirector {
    
    public void createMovie(MovieBuilder movieBuilder) {
        movieBuilder.addTittle("Pride and Prejudice");
        movieBuilder.addDirector("Joe Wright");
        movieBuilder.addMainActress("Keira Knightley");
        movieBuilder.addMainActor("Matthew Macfadyen");
        movieBuilder.addMinutes(120);
        movieBuilder.addYear(2005);
    }
}
